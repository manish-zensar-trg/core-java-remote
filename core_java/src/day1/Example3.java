package day1;

public class Example3 {

	public static void main(String[] args) {
		String name = "John";
		int age = 22;	
		
		System.out.println(name+" "+age);
		
		System.out.println("updating age");
		age = 27;
		System.out.println(name+" "+age);
		System.out.println("updating name..");
		name = "David";
		System.out.println(name+" "+age);
		
	}
}
