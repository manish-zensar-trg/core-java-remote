package day2.methods;

public class Example1 {

	public static void add() {
		int x = 4;
		int y = 5;
		int z = x+y;
		System.out.println(z);
	}
	
	public static void subtract() {
		int x = 14;
		int y = 8;
		int z = x-y;
		System.out.println(z);
	}
	
	public static void main(String[] args) {
		add();
		subtract();
		
	}
}
