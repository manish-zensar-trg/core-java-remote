package day2.methods;

public class Example2 {

	public static void add(int x, int y) {		
		int z = x+y;
		System.out.println(z);
	}
	
	public static void subtract(int x, int y) {
		int z = x-y;
		System.out.println(z);
	}
	
	public static void main(String[] args) {
		add(8, 5);
		subtract(27, 9);
		
	}
}
